import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in, "Cp866");

        ГорячихНапитковАвтомат автомат = new ГорячихНапитковАвтомат();

        ГорячийНапитокСТемпературой кофе = new ГорячийНапитокСТемпературой("Кофе", 250, 80);
        ГорячийНапитокСТемпературой чай = new ГорячийНапитокСТемпературой("Чай", 200, 75);
        ГорячийНапитокСТемпературой какао = new ГорячийНапитокСТемпературой("Какао", 300, 85);

        автомат.добавитьНапиток(кофе);
        автомат.добавитьНапиток(чай);
        автомат.добавитьНапиток(какао);

        System.out.println("Список доступных напитков:");
        автомат.показатьНапитки();

        System.out.println("Введите данные напитка для поиска:");
        System.out.print("Имя напитка: ");
        String имя = scanner.next();
        System.out.println("Объем напитка (мл): ");
        int объем = scanner.nextInt();
        System.out.println("Температура напитка (°C): ");
        int температура = scanner.nextInt();

        scanner.nextLine(); // Пропускаем символ новой строки

        ГорячийНапиток продукт = автомат.getProduct(имя, объем, температура);

        if (продукт != null) {
            System.out.println("Выданный продукт: " + продукт);
        } else {
            System.out.println("Продукт не найден");
        }

        scanner.close();
    }
}
